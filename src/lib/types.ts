// ─── Story & Content Types ───────────────────────────────────────────────────

export type StoryGroupStatus = 'active' | 'inactive' | 'test' | 'archived' | 'scheduled';

export type StoryStatus = 'active' | 'archived';

export type StoryType = 'image' | 'video';

export type AudienceType = 'none' | 'labels' | 'custom';

export type InteractiveComponentType = 'poll' | 'quiz' | 'cta' | 'emoji' | 'countdown' | 'swipe';

export interface InteractiveComponent {
  id: string;
  type: InteractiveComponentType;
  config: Record<string, unknown>;
}

export interface Story {
  id: string;
  title: string;
  thumbnail: string;
  videoUrl?: string;
  /** Tailwind gradient classes for template-based stories (e.g. "from-rose-500 to-orange-400") */
  gradient?: string;
  /** ID referencing a template component from the template registry */
  templateId?: string;
  status: StoryStatus;
  type: StoryType;
  interactiveComponents: InteractiveComponent[];
  /** Whether this story was generated by the AI generator */
  aiGenerated?: boolean;
  createdAt: string;
  updatedAt: string;
}

export interface Widget {
  id: string;
  name: string;
  platform: 'ios' | 'android' | 'web';
  appName: string;
  appIcon: string;
  token: string;
  createdAt: string;
}

export interface StoryGroup {
  id: string;
  title: string;
  coverImage: string;
  status: StoryGroupStatus;
  storiesCount: number;
  stories: Story[];
  audience: AudienceType;
  labels: string[];
  widgetIds: string[];
  schedule: {
    start: string | null;
    end: string | null;
  };
  isPinned: boolean;
  isSponsored: boolean;
  createdAt: string;
  updatedAt: string;
}

