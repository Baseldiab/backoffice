export interface ChartDataPoint {
  date: string;
  reach: number;
  impressions: number;
  clicks: number;
  activeUsers: number;
  eventTracker: number;
}

export type ChartMetric = 'reach' | 'impressions' | 'clicks' | 'activeUsers' | 'eventTracker';

export type ChartInterval = 'daily' | 'weekly' | 'monthly';

// ─── Activity Types ──────────────────────────────────────────────────────────

export type ActivityAction =
  | 'created'
  | 'updated'
  | 'published'
  | 'archived'
  | 'duplicated'
  | 'deleted'
  | 'scheduled';

export interface ActivityItem {
  id: string;
  action: ActivityAction;
  targetType: 'story_group' | 'story' | 'widget';
  targetName: string;
  userId: string;
  userName: string;
  userAvatar: string;
  timestamp: string;
}

// ─── Team & User Types ──────────────────────────────────────────────────────

export type UserRole = 'owner' | 'admin' | 'editor' | 'viewer';

export interface TeamMember {
  id: string;
  name: string;
  email: string;
  avatar: string;
  role: UserRole;
  lastActive: string;
  joinedAt: string;
}

// ─── Billing Types ──────────────────────────────────────────────────────────

export type PlanTier = 'free' | 'starter' | 'growth' | 'enterprise';

export type InvoiceStatus = 'paid' | 'pending' | 'overdue' | 'failed';

export interface BillingPlan {
  id: string;
  name: string;
  tier: PlanTier;
  price: number;
  currency: string;
  interval: 'monthly' | 'yearly';
  features: string[];
  limits: {
    storyGroups: number;
    storiesPerGroup: number;
    widgets: number;
    teamMembers: number;
    monthlyImpressions: number;
  };
}

export interface PaymentMethod {
  id: string;
  type: 'card' | 'bank';
  brand: string;
  last4: string;
  expiryMonth: number;
  expiryYear: number;
  isDefault: boolean;
}

export interface Invoice {
  id: string;
  number: string;
  amount: number;
  currency: string;
  status: InvoiceStatus;
  issuedAt: string;
  dueAt: string;
  paidAt: string | null;
  downloadUrl: string;
}

